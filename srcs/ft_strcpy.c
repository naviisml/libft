#include "../includes/libft.h"

/*
*
* The ft_strcpy() functions copy the string src 
* to dst (including the terminating `\0' character.)
*
*/

char	*ft_strcpy(char *dst, const char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
